package com.kh.day04.exercise;

public class Exercise_ForDouble {

	public static void main(String[] args) {
		//
		for(int i=1; i<=9; i++) {
			
			for(int j=2; j<=9; j++) {
				int sum = i*j;
				System.out.print(j + " * " + i + " = " + sum + "\t");
				
			}
			System.out.println();
			
		}
		
		
		
	}

}
